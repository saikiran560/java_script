//switch case
let day = "monday"

switch(day){
    case 'monday':
    case 'tuesday':
    case 'wednesday':
    case 'thursday':
    case 'fryday':
        console.log("7am");
        break;
    case 'saturday':
        console.log("11am");
        break;
    case 'sanday':
        console.log("11am");
        break;
    default :
        console.log(" You entered wrong input")
}